# int 
i = 39
print(type(i),i)

# float
f = float(3932482848292939.38383240)
print(type(f),f)

#complex
i = 3+4j
print(type(i),i)

# string
s = 'a';
print(type(s),s)

# boolean
b = 3==4
print(type(b),b)

# list
names = ["Rupesh Bhai","Kuch Nai","Denver"];
names[0]="master"
names.append("Denver is king")
print(type(names),names)

# tuple
names = ("Rupesh Bhai","Kuch Nai","Denver");
# names[0]="master"
print(type(names),names)

#dict
data = dict({"name":"gk","roll":399})
print(type(data),data)


#set
s = set({4,5,7,1,2,3,4,5,6,7,8,9,0,0,9,8,7,6,5,4,3,2,1});
print(type(s),s)


