# int 
i = 40
print(type(i),i)

# float
f = float(40.44)
print(type(f),f)

# string
s = 'a';
print(type(s),s)

# boolean
b = 3==4
print(type(b),b)

#complex
i = 10+14j
print(type(i),i)

# list
names = ["riya","supriya","siya"];
print(type(names),names)

# tuple
names = ("Rupesh Bhai","Kuch Nai","Denver");

