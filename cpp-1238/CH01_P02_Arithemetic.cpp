#include <iostream>
using namespace std;
int main()
{
    int a, b, c;

    cout<<"Enter the a number : ";
    cin>>a;

    cout<<"Enter the a number : ";
    cin>>b;

    c = a + b;
    cout << a << " + " << b << " = " << c << endl;

    c = a - b;
    cout << a << " - " << b << " = " << c << endl;

    return 0;
}