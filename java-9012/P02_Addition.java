public class P02_Addition {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        int c = a + b;

        // System.out.println("The Ans : "+c);
        System.out.println(a+" + "+b+" = "+c);

        c = a-b;
        System.out.println(a+" - "+b+" = "+c);

        c = a*b;
        System.out.println(a+" x "+b+" = "+c);

        c = a/b;
        System.out.println(a+"/"+b+" = "+c);

        c = a%b;
        System.out.println(a+"%"+b+" = "+c);

    }
}