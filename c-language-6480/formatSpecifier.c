#include <stdio.h>

void main()
{
    int numInt = 5;
    double numDouble = 5.2346789;
    float numFloat = 2.34;
    char ch = 'h';

    printf("Int format specifier is : %d\n", numInt);
    printf("Double format specifier is : %lf\n", numDouble);
    printf("Float format specifier is : %f\n", numFloat);
    printf("Char format specifier is : %c\n", ch);
}